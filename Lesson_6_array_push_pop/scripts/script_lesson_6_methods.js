// Методы, которые можно применить к массиву - встроенные в js готовые функции
let arr = [1, 2, 3, 4, 5];
// console.log(arr);

// Добавляет элемент в конец массива
arr.push(6);
// console.log(arr);

// Добавляет элемент в начало массива, в скобках указывается добавляемый элемент
arr.unshift(0);

// Извлекает элемент из конца массива
arr.pop();

// Извлекает элемент из начала массива
arr.shift();

arr.push('hello');
console.log(arr);

// Напишите цикл for, который создает массив из чисел от 1 до 10
let array = [];
for (let i = 1; i <= 10; i++) {
    array.push(i);
}
console.log(array);


